@dp.message(CommandStart())
async def cmd_start(message: types.Message):
    builder = InlineKeyboardBuilder()
    builder.row(types.InlineKeyboardButton(text="🎮 Заліпнути в гру", web_app=types.WebAppInfo(url=MINI_APP_URL)))
    
    greeting = random.choice(GREETINGS).format(name=message.from_user.first_name)
    await message.answer(
        f"{greeting}\n\nА якщо хочеш розім'яти пальці — кнопка на гру прямо під рукою 👇",
        reply_markup=builder.as_markup()
    )

@dp.message()
async def chat_with_friend(message: types.Message):
    # Імітуємо швидке та живе друкування тексту
    await bot.send_chat_action(message.chat.id, action="typing")
    await asyncio.sleep(random.uniform(0.3, 0.6))
    
    user_text = message.text.lower()
    chosen_response = None
    
    # Розумний семантичний пошук контексту за коренями слів
    for keyword, responses in CONTEXT_RESPONSES.items():
        if keyword in user_text:
            chosen_response = random.choice(responses)
            break
            
    # Якщо точного контексту немає — беремо одну з 20 унікальних харизматичних відповідей
    if not chosen_response:
        chosen_response = random.choice(UNIVERSAL_RESPONSES)
        
    builder = InlineKeyboardBuilder()
    builder.row(types.InlineKeyboardButton(text="🎮 Заліпнути в гру", web_app=types.WebAppInfo(url=MINI_APP_URL)))
    
    await message.answer(chosen_response, reply_markup=builder.as_markup())

